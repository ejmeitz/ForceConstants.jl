function calc_H3_exec_iik(pot::StillingerWeberSilicon, D)

    Symbolics.@variables ria, rib, ric, rja, rjb, rjc, rka, rkb, rkc

    r_i_vars = [ria, rib, ric]
    r_j_vars = [rja, rjb, rjc]
    r_k_vars = [rka, rkb, rkc]

    r_ij = [ria, rib, ric] .- [rja, rjb, rjc]
    r_ik = [ria, rib, ric] .- [rka, rkb, rkc]
    dist_ij = sqrt(sum(x -> x^2, r_ij))
    dist_ik = sqrt(sum(x -> x^2, r_ik))

    f_symbolic = three_body_potential_nounits(pot, r_ij, r_ik, dist_ij, dist_ik)

    H3_symbolic_iik = Array{RuntimeGeneratedFunction}(undef, D, D, D)
    r_vars = [r_i_vars; r_j_vars; r_k_vars]

    for a in 1:3
        for b in 1:3
            for c in 1:3
                D_iik = Differential(r_i_vars[a]) * Differential(r_i_vars[b]) * Differential(r_k_vars[c])
                H3_symbolic_iik[a,b,c] = Symbolics.build_function(expand_derivatives(D_iik(f_symbolic)), r_vars,
                                            expression = Val{false}; nanmath = false)
            end
        end
    end

    return H3_symbolic_iik
end

function H3_exec_symb!(block, H3, r_arr)

    for a in 1:3
        for b in 1:3
            for c in 1:3
                block[a,b,c] = H3[a,b,c](r_arr)
            end
        end
    end

    return block

end

